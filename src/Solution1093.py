class Solution:
    # min max mean mid mostcommon
    def sampleStats(self, count: [int]) -> [float]:
        cnts = 0
        smin = 255
        smax = 0
        sums = 0
        maxc = 0
        for i in range(len(count)):
            cnts += count[i]
            sums += count[i]*i
            if count[i] > 0:
                smin = min(smin, i)
                smax = max(smax, i)
                if count[i] > maxc:
                    maxc = count[i]
                    mostcommon = i
        mean = sums/cnts
        recnts = 0
        if cnts%2 == 0:
            midlp = int(cnts/2 - 1)
            midrp = int(cnts/2)
            for i in range(len(count)):
                if recnts <= midlp and midlp < recnts + count[i]:
                    mid = i
                if recnts <= midrp and midrp < recnts + count[i]:
                    mid += i
                    break
                recnts += count[i]
            mid = mid/2
        else:
            midp = int((cnts-1)/2)
            for i in range(len(count)):
                if recnts < midp and midp <= recnts + count[i]:
                    mid = i
                    break
                recnts += count[i]

        return [smin,smax, mean, mid, mostcommon]




